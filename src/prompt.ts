export function getModulePrompt(module: string): string {
  switch (module) {
    case 'interview':
      return INTERVIEW_PROMPT;
    case 'mock-date':
      return MOCK_DATE_PROMPT;
    case 'mission':
      return MISSION_PROMPT;
    case 'checkin':
      return CHECKIN_PROMPT;
    case 'date-plan':
      return DATE_PLAN_PROMPT;
    case 'debrief':
      return DEBRIEF_PROMPT;
    default:
      return '';
  }
}
